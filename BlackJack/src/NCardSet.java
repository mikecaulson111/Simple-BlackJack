

public class NCardSet
	{

			

				String Rank1;
				String Suit1;
				int Worth1;
				public NCardSet (String r, String s, int w)
				{
					Rank1 = r;
					Suit1 = s;
					Worth1 = w;
				}
				public String getRank1()
					{
						return Rank1;
					}
				public void setRank1(String rank1)
					{
						Rank1 = rank1;
					}
				public String getSuit1()
					{
						return Suit1;
					}
				public void setSuit1(String suit1)
					{
						Suit1 = suit1;
					}
				public int getWorth1()
					{
						return Worth1;
					}
				public void setWorth1(int worth1)
					{
						Worth1 = worth1;
					}
			

			


	}